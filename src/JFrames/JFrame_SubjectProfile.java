    private void deleting() {
        try {
            cst = con.prepareCall("{call sp_DeleteExistingSubject(?)}");
            cst.setInt(1, subjectId);
            if (cst.executeUpdate() == 1) {
                JOptionPane.showMessageDialog(this, "Successfully Deleted !", "ATTENTION", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }

    private void cancelling() {
        reset();
        retrieveCurrentInfo();
    }
