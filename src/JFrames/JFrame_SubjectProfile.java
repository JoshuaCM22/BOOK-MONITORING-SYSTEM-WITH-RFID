    private void validation() {
        if (txtSubject.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No Subject Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            txtSubject.requestFocus();
        } else {
            checkingSubject();
        }
    }

    private void checkingSubject() {
        try {
            pst = con.prepareStatement("SELECT * FROM tbl_subjects WHERE subject = ?");
            pst.setString(1, txtSubject.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "This Subject is already taken!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            } else {
                updating();
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }

    private void updating() {
        try {
            cst = con.prepareCall("{call sp_UpdateExistingSubject(?, ?)}");
            cst.setString("param_subject", txtSubject.getText());
            cst.setInt("param_id", subjectId);
            if (cst.executeUpdate() == 1) {
                JOptionPane.showMessageDialog(this, "Successfully Updated!", "ATTENTION", JOptionPane.INFORMATION_MESSAGE);
                reset();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }

    private void reset() {
        try {
            txtSubject.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
            lblSubject.requestFocus();
            txtSubject.setFocusable(false);
            btnClose.setEnabled(true);
            btnEditOrUpdate.setText("EDIT");
            btnDeleteOrCancel.setText("DELETE");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }
    private void retrieveCurrentInfo() {
        try {
            pst = con.prepareStatement("SELECT * FROM tbl_subjects WHERE id = ?");
            pst.setInt(1, subjectId);
            rs = pst.executeQuery();
            if (rs.next()) {
                txtSubject.setText(rs.getString(2));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }

