    private void computingAge() {
        try {
            String monthValue = (String) cmbMonth.getSelectedItem();
            String dayValue = (String) cmbDay.getSelectedItem();
            String yearValue = txtYear.getText();
            String converting = monthValue;
            switch (converting) {
                case ("January"):
                    converting = "01";
                    break;
                case ("February"):
                    converting = "02";
                    break;
                case ("March"):
                    converting = "03";
                    break;
                case ("April"):
                    converting = "04";
                    break;
                case ("May"):
                    converting = "05";
                    break;
                case ("June"):
                    converting = "06";
                    break;
                case ("July"):
                    converting = "07";
                    break;
                case ("August"):
                    converting = "08";
                    break;
                case ("September"):
                    converting = "09";
                    break;
                case ("October"):
                    converting = "10";
                    break;
                case ("November"):
                    converting = "11";
                    break;
                case ("December"):
                    converting = "12";
                    break;
            }
            if (converting != "Month" && dayValue != "Day" && yearValue != "") {
                int day = Integer.parseInt(dayValue);
                int month = Integer.parseInt(converting);
                int year = Integer.parseInt(yearValue);
                LocalDate selectedDate = LocalDate.of(year, month, day);
                LocalDate currentDate = LocalDate.now();
                int resultYear = Period.between(selectedDate, currentDate).getYears();
                txtAge.setText("" + resultYear);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }

    private void validation() {
        if (txtRFID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No RFID Found!", "ATTENTIOM", JOptionPane.ERROR_MESSAGE);
            txtRFID.requestFocus();
        } else if (txtLastName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No Last Name Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            txtLastName.requestFocus();
        } else if (txtGivenName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No Given Name Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            txtGivenName.requestFocus();
        } else if (txtMiddleInitial.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No Middle Initial Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            txtMiddleInitial.requestFocus();
        } else if (rbtnMale.isSelected() == false && rbtnFemale.isSelected() == false) {
            JOptionPane.showMessageDialog(this, "No Gender Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
        } else if (cmbMonth.getSelectedItem().equals("Month")) {
            JOptionPane.showMessageDialog(this, "No Month Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            cmbMonth.requestFocus();
        } else if (cmbDay.getSelectedItem().equals("Day")) {
            JOptionPane.showMessageDialog(this, "No Day Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            cmbDay.requestFocus();
        } else if (txtYear.getText().equals("Year")) {
            JOptionPane.showMessageDialog(this, "No Year Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            txtYear.requestFocus();
        } else if (txtAge.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No Age Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            txtAge.requestFocus();
        } else if (txtAddress.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No Address Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            txtAddress.requestFocus();
        } else if (cmbGradeLevel.getSelectedItem().equals("None")) {
            JOptionPane.showMessageDialog(this, "No Grade Level Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            cmbGradeLevel.requestFocus();
        } else if (txtContactNumber.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No Contact Number Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            txtContactNumber.requestFocus();
        } else {
            checkingRFID();
        }
    }

    private void checkingRFID() {
        try {
            pst = con.prepareStatement("SELECT * FROM tbl_students WHERE rfid = ?");
            pst.setString(1, txtRFID.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "This RFId is already taken!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            } else {
                creatingStudent();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }

    private void creatingStudent() {
        try {
            String month = (String) cmbMonth.getSelectedItem();
            String day = cmbDay.getSelectedItem().toString();
            String year = txtYear.getText();
            combination = year + "-" + month + "-" + day;
            if (imagePath != null) {
                DateFormat pattern = new SimpleDateFormat("yyyy-MMMM-dd");
                Date myDate = pattern.parse(combination);
                java.sql.Date converted = new java.sql.Date(myDate.getTime());
                char space = ' ';
                String fullName;
                if (txtSuffix.getText().equals("")) {
                    fullName = txtLastName.getText() + "," + space + txtGivenName.getText() + space + txtMiddleInitial.getText() + ".";
                } else {
                    fullName = txtLastName.getText() + "," + space + txtGivenName.getText() + space + txtMiddleInitial.getText() + "." + space + txtSuffix.getText() + ".";
                }
                cst = con.prepareCall("{call sp_InsertNewStudent(?, ?, ?, ?, ?, ?, ?, ?, ?, ?)}");
                cst.setString(1, txtRFID.getText());
                cst.setString(2, fullName);
                cst.setInt(3, gender);
                cst.setDate(4, converted);
                cst.setString(5, txtAge.getText());
                cst.setString(6, txtAddress.getText());
                cst.setInt(7, gradeLevel);
                cst.setString(8, txtContactNumber.getText());
                InputStream inputStream = new FileInputStream(new File(imagePath));
                cst.setBlob(9, inputStream);
                cst.setInt(10, 1);
                if (cst.executeUpdate() == 1) {
                    JOptionPane.showMessageDialog(this, "Successfully Added!", "ATTENTION", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                    btnBack.setText("BACK");
                }
            } else {
                JOptionPane.showMessageDialog(this, "No Photo Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
                btnBrowseClicked();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }
