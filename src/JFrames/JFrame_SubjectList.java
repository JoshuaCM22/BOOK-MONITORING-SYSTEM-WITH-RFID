    private void retrieveShelfInformations() {
        try {
            JFrame_SubjectProfile.subjectId = Integer.parseInt((this.tblSubjectList.getValueAt(this.tblSubjectList.getSelectedRow(), 0).toString()));
            pst = con.prepareStatement("SELECT * FROM tbl_subjects WHERE id = ?");
            pst.setInt(1, JFrame_SubjectProfile.subjectId);
            rs = pst.executeQuery();
            if (rs.next()) {
                JFrame frame = this;
                frame.setEnabled(false);
                JFrame_SubjectProfile nextFrame = new JFrame_SubjectProfile();
                btnBack.setVisible(false);
                btnAddNewSubject.setVisible(false);
                txtSearch.setVisible(false);
                nextFrame.setLocationRelativeTo(this);
                nextFrame.addWindowListener(new java.awt.event.WindowAdapter() {
                    @Override
                    public void windowClosed(java.awt.event.WindowEvent windowEvent) {
                        if (txtSearch.getText().equals("")) {
                            setShelfsInformationForTable();
                        } else {
                            searching();
                        }
                        frame.setEnabled(true);
                        frame.toFront();
                        btnBack.setVisible(true);
                        btnAddNewSubject.setVisible(true);
                        txtSearch.setVisible(true);
                        txtSearch.requestFocus();
                    }
                });
                nextFrame.txtSubject.setText(rs.getString(2));
                nextFrame.toFront();
                nextFrame.setVisible(true);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }

    private void searching() {
        if (txtSearch.getText().equals("")) {
            setShelfsInformationForTable();
        } else {
            try {
                int rows = 0;
                int rowIndex = 0;
                cst = con.prepareCall("{call sp_GetSubjectBySubject(?)}");
                cst.setString("param_subject", "%" + txtSearch.getText() + "%");
                rs = cst.executeQuery();
                if (rs.next()) {
                    rs.last();
                    rows = rs.getRow();
                    rs.beforeFirst();
                }
                String[][] data = new String[rows][8];
                while (rs.next()) {
                    data[rowIndex][0] = rs.getInt(1) + "";
                    data[rowIndex][1] = rs.getString(2);

                    rowIndex++;
                }
                String[] cols = {"ID", "SUBJECT"};
                DefaultTableModel model = new DefaultTableModel(data, cols);
                tblSubjectList.setRowHeight(25);
                tblSubjectList.setModel(model);
                DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
                centerRenderer.setHorizontalAlignment(JLabel.CENTER);
                tblSubjectList.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
                tblSubjectList.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
                tblSubjectList.getColumnModel().getColumn(0).setWidth(0);
                tblSubjectList.getColumnModel().getColumn(0).setMinWidth(0);
                tblSubjectList.getColumnModel().getColumn(0).setMaxWidth(0);
                tblSubjectList.getColumnModel().getColumn(1).setPreferredWidth(741);
                tblSubjectList.setSelectionBackground(Color.ORANGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex);
                ex.printStackTrace();
            }
        }
        this.lblTotalEntry.setText("TOTAL ENTRY: " + tblSubjectList.getRowCount());
    }

