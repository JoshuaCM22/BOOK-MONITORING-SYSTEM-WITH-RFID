    private void setShelfsInformationForTable() {
        try {
            int rows = 0;
            int rowIndex = 0;
            pst = con.prepareStatement("SELECT * FROM vw_getallsubjects");
            rs = pst.executeQuery();
            if (rs.next()) {
                rs.last();
                rows = rs.getRow();
                rs.beforeFirst();
            }
            String[][] data = new String[rows][14];
            while (rs.next()) {
                data[rowIndex][0] = rs.getInt(1) + "";
                data[rowIndex][1] = rs.getString(2);
                rowIndex++;
            }
            String[] cols = {"ID", "SUBJECT"};
            DefaultTableModel model = new DefaultTableModel(data, cols);
            tblSubjectList.setRowHeight(25);
            tblSubjectList.setModel(model);
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(JLabel.CENTER);
            tblSubjectList.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
            tblSubjectList.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
            tblSubjectList.getColumnModel().getColumn(0).setWidth(0);
            tblSubjectList.getColumnModel().getColumn(0).setMinWidth(0);
            tblSubjectList.getColumnModel().getColumn(0).setMaxWidth(0);
            tblSubjectList.getColumnModel().getColumn(1).setPreferredWidth(741);
            tblSubjectList.setSelectionBackground(Color.ORANGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
        this.lblTotalEntry.setText("TOTAL ENTRY: " + tblSubjectList.getRowCount());
    }


